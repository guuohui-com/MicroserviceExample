public class Routes {
   private String id;
   private String path;
   private String uri;

    public Routes() {
    }

    public Routes(String id, String path, String uri) {
        this.id = id;
        this.path = path;
        this.uri = uri;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getUri() {
        return uri;
    }

    public void setUri(String uri) {
        this.uri = uri;
    }
}
